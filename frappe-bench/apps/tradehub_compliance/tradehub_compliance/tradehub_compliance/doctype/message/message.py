"""
Message DocType for Trade Hub B2B Marketplace.

This module implements individual messages within conversation threads.
Messages can be sent between buyers and sellers, or generated by the system
for notifications and updates.

Key features:
- Multi-tenant data isolation via parent thread's tenant
- Message types: Text, Rich Text, System Notification, Auto Reply, etc.
- Status workflow: Draft -> Sent -> Delivered -> Read
- Attachment support with JSON storage
- Reply threading support
- Soft delete capability for moderation
- Read receipts and delivery tracking
- fetch_from pattern for thread, sender, buyer, and seller information
"""

import frappe
from frappe import _
from frappe.model.document import Document
from frappe.utils import nowdate, now_datetime, cint, strip_html
import json


# Valid status transitions
STATUS_TRANSITIONS = {
    "Draft": ["Sent", "Failed"],
    "Sent": ["Delivered", "Read", "Failed"],
    "Delivered": ["Read"],
    "Read": [],
    "Failed": ["Sent"]
}

# System message types (auto-generated messages)
SYSTEM_MESSAGE_TYPES = [
    "System Notification",
    "Auto Reply",
    "Quotation Response",
    "RFQ Response",
    "Order Update",
    "Shipping Update"
]


class Message(Document):
    """
    Message DocType for individual messages in conversation threads.

    Each Message represents a single message within a Message Thread.
    Features include:
    - Link to Message Thread with auto-fetched thread info and tenant
    - Sender information (User, Buyer, or Seller)
    - Rich text content with plain text extraction
    - Attachment support via JSON field
    - Reply threading support
    - Read receipts and delivery tracking
    - Soft delete for moderation
    """

    def before_insert(self):
        """Set defaults before inserting a new message."""
        self.set_sent_timestamp()
        self.set_sender_from_user()
        self.extract_plain_text()
        self.process_attachments()
        self.set_system_message_flag()

    def validate(self):
        """Validate message data before saving."""
        self.validate_message_thread()
        self.validate_sender()
        self.validate_content()
        self.validate_reply_to()
        self.validate_status_transition()
        self.validate_tenant_isolation()
        self.update_attachment_flag()

    def on_update(self):
        """Actions after message is saved."""
        self.update_thread_stats()
        self.send_notification_if_needed()
        self.clear_message_cache()

    def after_insert(self):
        """Actions after message is inserted."""
        self.update_thread_on_new_message()

    def on_trash(self):
        """Actions before message is deleted."""
        self.check_deletion_permissions()

    # =========================================================================
    # DEFAULT SETTINGS
    # =========================================================================

    def set_sent_timestamp(self):
        """Set sent timestamp for new messages."""
        if not self.sent_at and self.status != "Draft":
            self.sent_at = now_datetime()

    def set_sender_from_user(self):
        """
        Set sender information from current user if not provided.
        """
        if not self.sender_user:
            self.sender_user = frappe.session.user

        # Auto-detect sender type
        if not self.buyer and not self.seller:
            user = frappe.session.user

            # Check if user is a buyer
            buyer = frappe.db.get_value(
                "Buyer Profile",
                {"user": user, "status": ["!=", "Inactive"]},
                "name"
            )
            if buyer:
                self.buyer = buyer
                self.sender_type = "Buyer"
                return

            # Check if user is a seller
            seller = frappe.db.get_value(
                "Seller Profile",
                {"user": user, "status": ["!=", "Inactive"]},
                "name"
            )
            if seller:
                self.seller = seller
                self.sender_type = "Seller"
                return

    def extract_plain_text(self):
        """Extract plain text from rich text content for search and notifications."""
        if self.content:
            self.plain_text_content = strip_html(self.content)[:5000]

    def process_attachments(self):
        """Process and validate attachments JSON."""
        if self.attachments:
            try:
                if isinstance(self.attachments, str):
                    attachments = json.loads(self.attachments)
                else:
                    attachments = self.attachments

                # Validate attachment structure
                valid_attachments = []
                for att in attachments:
                    if isinstance(att, dict) and "file_url" in att:
                        valid_attachments.append({
                            "file_url": att.get("file_url"),
                            "file_name": att.get("file_name", "attachment"),
                            "file_type": att.get("file_type", "unknown"),
                            "file_size": att.get("file_size", 0)
                        })

                self.attachments = json.dumps(valid_attachments)
                self.has_attachments = len(valid_attachments) > 0
            except (json.JSONDecodeError, TypeError):
                self.attachments = "[]"
                self.has_attachments = 0

    def set_system_message_flag(self):
        """Set system message flag based on message type."""
        if self.message_type in SYSTEM_MESSAGE_TYPES:
            self.is_system_message = 1

    # =========================================================================
    # VALIDATION METHODS
    # =========================================================================

    def validate_message_thread(self):
        """Validate message thread exists and is accessible."""
        if not self.message_thread:
            frappe.throw(_("Message Thread is required"))

        thread_status = frappe.db.get_value(
            "Message Thread", self.message_thread, "status"
        )

        if thread_status == "Archived" and not self.is_system_message:
            frappe.throw(
                _("Cannot add messages to archived thread {0}").format(
                    self.message_thread
                )
            )

        # Reactivate closed threads on new message (handled by thread)
        if thread_status == "Closed" and self.is_new():
            frappe.msgprint(
                _("Thread will be reactivated with this new message"),
                indicator='blue',
                alert=True
            )

    def validate_sender(self):
        """Validate sender information is provided."""
        if self.sender_type == "System":
            return  # System messages don't need buyer/seller

        if self.sender_type == "Buyer" and not self.buyer:
            frappe.throw(_("Buyer is required when Sender Type is Buyer"))

        if self.sender_type == "Seller" and not self.seller:
            frappe.throw(_("Seller is required when Sender Type is Seller"))

    def validate_content(self):
        """Validate message content is provided."""
        if not self.content or not self.content.strip():
            frappe.throw(_("Message content is required"))

        # Update plain text if content changed
        if self.has_value_changed("content"):
            self.extract_plain_text()

    def validate_reply_to(self):
        """Validate reply_to message belongs to same thread."""
        if not self.reply_to:
            return

        reply_thread = frappe.db.get_value(
            "Message", self.reply_to, "message_thread"
        )
        if reply_thread != self.message_thread:
            frappe.throw(
                _("Reply-to message must belong to the same thread")
            )

    def validate_status_transition(self):
        """Validate status transitions are valid."""
        if self.is_new():
            return

        old_status = frappe.db.get_value("Message", self.name, "status")
        if old_status and old_status != self.status:
            valid_transitions = STATUS_TRANSITIONS.get(old_status, [])
            if self.status not in valid_transitions:
                frappe.throw(
                    _("Cannot change status from {0} to {1}. "
                      "Valid transitions are: {2}").format(
                        old_status, self.status,
                        ", ".join(valid_transitions) if valid_transitions else "None"
                    )
                )

    def validate_tenant_isolation(self):
        """
        Validate that message belongs to user's tenant.

        Inherits tenant from Message Thread to ensure multi-tenant data isolation.
        """
        if not self.tenant:
            return

        # System Manager can access all tenants
        if "System Manager" in frappe.get_roles():
            return

        # Get current user's tenant
        from tradehub_core.tradehub_core.utils.tenant import get_current_tenant
        current_tenant = get_current_tenant()

        if current_tenant and self.tenant != current_tenant:
            frappe.throw(
                _("Access denied: You can only access messages in your tenant")
            )

    def update_attachment_flag(self):
        """Update has_attachments flag based on attachments JSON."""
        if self.attachments:
            try:
                attachments = json.loads(self.attachments) if isinstance(
                    self.attachments, str
                ) else self.attachments
                self.has_attachments = 1 if attachments else 0
            except (json.JSONDecodeError, TypeError):
                self.has_attachments = 0
        else:
            self.has_attachments = 0

    # =========================================================================
    # STATUS MANAGEMENT
    # =========================================================================

    def set_status(self, new_status):
        """
        Change the status of the message.

        Args:
            new_status: The new status to set

        Returns:
            bool: True if status was changed successfully
        """
        valid_transitions = STATUS_TRANSITIONS.get(self.status, [])
        if new_status not in valid_transitions:
            frappe.throw(
                _("Cannot change status from {0} to {1}").format(
                    self.status, new_status
                )
            )

        self.status = new_status

        if new_status == "Sent" and not self.sent_at:
            self.sent_at = now_datetime()
        elif new_status == "Delivered":
            self.is_delivered = 1
            self.delivered_at = now_datetime()
        elif new_status == "Read":
            self.is_read = 1
            self.read_at = now_datetime()

        self.save()
        return True

    def mark_as_sent(self):
        """Mark message as sent."""
        return self.set_status("Sent")

    def mark_as_delivered(self):
        """Mark message as delivered."""
        return self.set_status("Delivered")

    def mark_as_read(self):
        """Mark message as read."""
        return self.set_status("Read")

    def mark_as_failed(self, error_message=None):
        """
        Mark message as failed.

        Args:
            error_message: Optional error description
        """
        if error_message:
            self.delivery_error = error_message
        self.status = "Failed"
        self.save()

    # =========================================================================
    # MESSAGE EDITING
    # =========================================================================

    def edit_content(self, new_content):
        """
        Edit the message content.

        Args:
            new_content: The new message content

        Returns:
            bool: True if edit was successful
        """
        if self.is_deleted:
            frappe.throw(_("Cannot edit a deleted message"))

        if self.is_system_message:
            frappe.throw(_("Cannot edit system messages"))

        self.content = new_content
        self.extract_plain_text()
        self.is_edited = 1
        self.edited_at = now_datetime()
        self.save()
        return True

    # =========================================================================
    # SOFT DELETE
    # =========================================================================

    def soft_delete(self, reason=None):
        """
        Soft delete the message.

        Args:
            reason: Optional reason for deletion
        """
        self.is_deleted = 1
        self.deleted_at = now_datetime()
        self.deleted_by = frappe.session.user
        if reason:
            self.flag_reason = reason
        self.save()

    def restore(self):
        """Restore a soft-deleted message."""
        self.is_deleted = 0
        self.deleted_at = None
        self.deleted_by = None
        self.save()

    # =========================================================================
    # FLAGGING
    # =========================================================================

    def flag_message(self, reason):
        """
        Flag the message for review.

        Args:
            reason: Reason for flagging
        """
        self.is_flagged = 1
        self.flag_reason = reason
        self.save()

    def unflag_message(self):
        """Remove flag from message."""
        self.is_flagged = 0
        self.flag_reason = None
        self.save()

    # =========================================================================
    # THREAD INTEGRATION
    # =========================================================================

    def update_thread_on_new_message(self):
        """Update parent thread statistics when new message is added."""
        if not self.message_thread:
            return

        thread = frappe.get_doc("Message Thread", self.message_thread)

        # Get display name for sender
        sender_display = self.sender_name or self.buyer_name or self.seller_name or "System"

        thread.update_message_stats(
            message_content=self.plain_text_content,
            message_by=sender_display
        )

    def update_thread_stats(self):
        """Update thread stats on message update."""
        pass  # Main update handled in after_insert

    # =========================================================================
    # NOTIFICATIONS
    # =========================================================================

    def send_notification_if_needed(self):
        """Send notification if message is newly sent."""
        if self.is_new() and self.status == "Sent" and not self.notification_sent:
            self.send_notification()

    def send_notification(self):
        """
        Send notification to recipient.

        This is a placeholder for actual notification logic.
        """
        # Get thread details
        thread = frappe.get_cached_doc("Message Thread", self.message_thread)

        # Skip notification for system messages if disabled
        if self.is_system_message and not thread.notification_enabled:
            return

        # Future: Implement actual notification sending
        # - Email notification via frappe.sendmail
        # - Push notification via PWA service worker
        # - SMS notification via SMS provider

        self.db_set("notification_sent", 1)

    # =========================================================================
    # DELETION CHECKS
    # =========================================================================

    def check_deletion_permissions(self):
        """Check if message can be permanently deleted."""
        if not frappe.has_permission("Message", "delete"):
            frappe.throw(
                _("You don't have permission to delete messages. "
                  "Use soft delete instead.")
            )

    # =========================================================================
    # CACHE MANAGEMENT
    # =========================================================================

    def clear_message_cache(self):
        """Clear cached message data."""
        cache_keys = [
            f"message:{self.name}",
            f"thread_messages:{self.message_thread}",
        ]

        if self.buyer:
            cache_keys.append(f"buyer_messages:{self.buyer}")

        if self.seller:
            cache_keys.append(f"seller_messages:{self.seller}")

        for key in cache_keys:
            frappe.cache().delete_value(key)


