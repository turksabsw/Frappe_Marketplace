@frappe.whitelist()
def get_thread_messages(message_thread, include_deleted=False, limit=50, offset=0):
    """
    Get all messages in a thread.

    Args:
        message_thread: The Message Thread name
        include_deleted: Include soft-deleted messages (default False)
        limit: Maximum messages to return (default 50)
        offset: Offset for pagination (default 0)

    Returns:
        list: List of message records
    """
    filters = {"message_thread": message_thread}

    if not include_deleted:
        filters["is_deleted"] = 0

    messages = frappe.get_all(
        "Message",
        filters=filters,
        fields=[
            "name", "message_type", "status", "sender_type",
            "sender_name", "buyer_name", "seller_name",
            "content", "plain_text_content", "has_attachments",
            "attachments", "sent_at", "is_read", "read_at",
            "is_edited", "is_system_message", "reply_to",
            "reply_to_preview"
        ],
        order_by="sent_at asc",
        limit_page_length=cint(limit),
        limit_start=cint(offset)
    )

    return messages


@frappe.whitelist()
def send_message(message_thread, content, message_type="Text",
                 attachments=None, reply_to=None):
    """
    Send a new message to a thread.

    Args:
        message_thread: The Message Thread name
        content: Message content (can be HTML for rich text)
        message_type: Type of message (default Text)
        attachments: Optional JSON array of attachments
        reply_to: Optional message name to reply to

    Returns:
        dict: Created message info
    """
    doc = frappe.new_doc("Message")
    doc.message_thread = message_thread
    doc.content = content
    doc.message_type = message_type
    doc.status = "Sent"
    doc.reply_to = reply_to

    if attachments:
        doc.attachments = attachments if isinstance(
            attachments, str
        ) else json.dumps(attachments)

    doc.insert()

    return {
        "name": doc.name,
        "message": _("Message sent successfully"),
        "status": doc.status,
        "sent_at": str(doc.sent_at)
    }


@frappe.whitelist()
def mark_message_as_read(message_name):
    """
    Mark a message as read.

    Args:
        message_name: The Message document name

    Returns:
        dict: Success message
    """
    doc = frappe.get_doc("Message", message_name)

    if doc.status != "Read":
        doc.mark_as_read()

    return {
        "success": True,
        "message": _("Message marked as read"),
        "read_at": str(doc.read_at)
    }


@frappe.whitelist()
def mark_thread_messages_as_read(message_thread, reader_type, reader_profile):
    """
    Mark all messages in a thread as read for a specific reader.

    Args:
        message_thread: The Message Thread name
        reader_type: 'buyer' or 'seller'
        reader_profile: The profile name of the reader

    Returns:
        dict: Number of messages marked as read
    """
    # Get unread messages not sent by the reader
    filters = {
        "message_thread": message_thread,
        "is_read": 0,
        "is_deleted": 0
    }

    if reader_type == "buyer":
        filters["buyer"] = ["!=", reader_profile]
    else:
        filters["seller"] = ["!=", reader_profile]

    messages = frappe.get_all("Message", filters=filters, fields=["name"])

    count = 0
    for msg in messages:
        doc = frappe.get_doc("Message", msg.name)
        doc.is_read = 1
        doc.read_at = now_datetime()
        doc.status = "Read"
        doc.save(ignore_permissions=True)
        count += 1

    # Update thread unread count
    thread = frappe.get_doc("Message Thread", message_thread)
    thread.mark_as_read()

    return {
        "success": True,
        "marked_count": count,
        "message": _("{0} messages marked as read").format(count)
    }


