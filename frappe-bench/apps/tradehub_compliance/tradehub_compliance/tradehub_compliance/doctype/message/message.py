@frappe.whitelist()
def get_message_statistics(message_thread=None, buyer=None, seller=None,
                            date_from=None, date_to=None):
    """
    Get message statistics.

    Args:
        message_thread: Optional thread filter
        buyer: Optional buyer filter
        seller: Optional seller filter
        date_from: Start date for statistics
        date_to: End date for statistics

    Returns:
        dict: Statistics including counts, read rates, etc.
    """
    filters = {"is_deleted": 0}

    if message_thread:
        filters["message_thread"] = message_thread
    if buyer:
        filters["buyer"] = buyer
    if seller:
        filters["seller"] = seller
    if date_from:
        filters["sent_at"] = [">=", date_from]
    if date_to:
        if "sent_at" in filters:
            filters["sent_at"] = ["between", [date_from, date_to]]
        else:
            filters["sent_at"] = ["<=", date_to]

    # Get total count
    total = frappe.db.count("Message", filters)

    # Get read count
    read_filters = {**filters, "is_read": 1}
    read_count = frappe.db.count("Message", read_filters)

    # Get counts by status
    status_counts = frappe.db.get_all(
        "Message",
        filters=filters,
        fields=["status", "count(*) as count"],
        group_by="status"
    )

    status_dict = {s.status: s.count for s in status_counts}

    # Get counts by sender type
    sender_counts = frappe.db.get_all(
        "Message",
        filters=filters,
        fields=["sender_type", "count(*) as count"],
        group_by="sender_type"
    )

    sender_dict = {s.sender_type: s.count for s in sender_counts}

    # Get counts by message type
    type_counts = frappe.db.get_all(
        "Message",
        filters=filters,
        fields=["message_type", "count(*) as count"],
        group_by="message_type"
    )

    type_dict = {t.message_type: t.count for t in type_counts}

    return {
        "total_messages": total,
        "read_count": read_count,
        "read_rate": (read_count / total * 100) if total > 0 else 0,
        "status_breakdown": status_dict,
        "sender_breakdown": sender_dict,
        "type_breakdown": type_dict
    }
